/*

There are ONLY SIX Relational Operators

This is the complete list.
---------------------------------
  Operator	      Meaning
---------------------------------
    > 	          Greater Than
    <             Less Than
    >=            Greater Than or Equal To
    <=	          Less Than or Equal To
    ==	          Equal To
    !=	          Not Equal To
---------------------------------
*/

#include <stdio.h>

int main(){

printf("%d\n",5>2);
printf("%d\n",4<2);
printf("%d\n",4>=7);
printf("%d\n",3<=6);
printf("%d\n",5==5);
printf("%d\n",6!=6);

return 0;

}

